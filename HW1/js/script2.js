'use script'
var numb = parseInt(prompt('введите однозначное положительное число'));
switch (numb) {
	case 0:
		console.log('ноль');
		break;
	case 1:
		console.log('один');
		break;
	case 2:
		console.log('два');
		break;
	case 3:
		console.log('три');
		break;
	case 4:
		console.log('четыре');
		break;
	case 5:
		console.log('пять');
		break;
	case 6:
		console.log('шесть');
		break;
	case 7:
		console.log('семь');
		break;
	case 8:
		console.log('восемь');
		break;
	case 9:
		console.log('девять');
		break;
	default:
		console.log('это не однозначное число');
}